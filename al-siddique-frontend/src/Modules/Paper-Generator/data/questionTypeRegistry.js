//  Derived helpers 
export const REGISTRY_MAP = Object.fromEntries(QUESTION_TYPE_REGISTRY.map(t => [t.value, t]))

export const getTypeByValue = value => REGISTRY_MAP[value] || REGISTRY_MAP['short']

// formTypes that need StructuredDataEditor in QuestionModal
export const STRUCTURED_DATA_FORM_TYPES = new Set(['wahid_jama', 'sentence_usage', 'comprehension', 'numerical', 'diagram'])

// Types where medium should default to 'urdu'
export const URDU_DEFAULT_TYPES = new Set(
 QUESTION_TYPE_REGISTRY.filter(t => t.defaultMedium === 'urdu').map(t => t.value)
)

// Types where the answer field is shown (exclude MCQ, T/F, columns, wahid_jama, sentence_usage)
export const SHOW_ANSWER_FIELD = new Set(
 QUESTION_TYPE_REGISTRY
 .filter(t => !['mcq', 'true_false', 'columns', 'wahid_jama', 'sentence_usage'].includes(t.value))
 .map(t => t.value)
)

